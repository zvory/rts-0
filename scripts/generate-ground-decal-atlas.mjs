#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { PNG } from "pngjs";

import { GROUND_DECAL_ASSET_MANIFEST } from "../client/src/renderer/decals/manifest.js";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const outputPng = path.join(root, "client/assets/decals/ground-decals-v1.png");
const outputManifest = path.join(root, "client/src/renderer/decals/atlas.generated.js");
const check = process.argv.includes("--check");
const chrome = process.env.RTS_CHROME_PATH
  || "/Applications/Google Chrome.app/Contents/MacOS/Google Chrome";
const assets = Object.entries(GROUND_DECAL_ASSET_MANIFEST).flatMap(([group, entries]) => (
  entries.map((asset) => ({
    group,
    id: asset.id,
    width: asset.width,
    height: asset.height,
    sourcePath: path.join(root, "client", asset.url.slice(1)),
  }))
));

let x = 0;
const rects = assets.map((asset) => {
  const rect = { group: asset.group, id: asset.id, x, y: 0, width: asset.width, height: asset.height };
  x += asset.width;
  return rect;
});
const atlasWidth = x;
const atlasHeight = Math.max(...assets.map((asset) => asset.height));
const png = await renderAtlasPng();

const grouped = Object.fromEntries(Object.keys(GROUND_DECAL_ASSET_MANIFEST).map((group) => [
  group,
  rects.filter((rect) => rect.group === group).map(({ group: _group, ...rect }) => rect),
]));
const manifest = `// Generated by scripts/generate-ground-decal-atlas.mjs.\n`
  + `export const GROUND_DECAL_PNG_ATLAS = Object.freeze(${JSON.stringify({
    version: 1,
    url: "/assets/decals/ground-decals-v1.png",
    width: atlasWidth,
    height: atlasHeight,
    groups: grouped,
  }, null, 2)});\n`;

if (check) {
  if (!fs.existsSync(outputPng) || !fs.existsSync(outputManifest)) process.exitCode = 1;
  else if (!fs.readFileSync(outputPng).equals(png) || fs.readFileSync(outputManifest, "utf8") !== manifest) process.exitCode = 1;
  if (process.exitCode) throw new Error("ground decal atlas is stale; run scripts/generate-ground-decal-atlas.mjs");
} else {
  fs.writeFileSync(outputPng, png);
  fs.writeFileSync(outputManifest, manifest);
  console.log(`wrote ${path.relative(root, outputPng)} (${atlasWidth}x${atlasHeight})`);
}

async function renderAtlasPng() {
  if (!fs.existsSync(chrome)) throw new Error(`Chrome executable not found at ${chrome}`);
  const imported = await import("puppeteer-core");
  const puppeteer = imported.default || imported;
  const browser = await puppeteer.launch({
    executablePath: chrome,
    headless: "new",
    defaultViewport: { width: Math.max(800, atlasWidth), height: Math.max(600, atlasHeight) },
    args: ["--no-sandbox", `--window-size=${Math.max(800, atlasWidth)},${Math.max(600, atlasHeight)}`],
  });
  try {
    const page = await browser.newPage();
    const sources = assets.map((asset, index) => ({
      x: rects[index].x,
      width: asset.width,
      height: asset.height,
      dataUrl: `data:image/svg+xml;base64,${fs.readFileSync(asset.sourcePath).toString("base64")}`,
    }));
    const dataUrl = await page.evaluate(async ({ width, height, sources: svgSources }) => {
      const canvas = document.createElement("canvas");
      canvas.width = width;
      canvas.height = height;
      const context = canvas.getContext("2d", { alpha: true });
      context.imageSmoothingEnabled = false;
      context.clearRect(0, 0, width, height);
      for (const source of svgSources) {
        const image = new Image();
        image.src = source.dataUrl;
        await image.decode();
        context.drawImage(image, source.x, 0, source.width, source.height);
      }
      return canvas.toDataURL("image/png");
    }, { width: atlasWidth, height: atlasHeight, sources });
    await page.close();
    const browserPng = PNG.sync.read(Buffer.from(dataUrl.slice(dataUrl.indexOf(",") + 1), "base64"));
    return PNG.sync.write(browserPng, { colorType: 6, inputColorType: 6, inputHasAlpha: true });
  } finally {
    await browser.close();
  }
}
